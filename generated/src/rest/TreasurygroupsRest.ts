import * as express from "express";
import { Request, Response } from "express";
import IControllerBase from "../framework/IControllerBase.interface";
import { getCustomRepository } from "typeorm";

import { TreasurygroupsRepository } from "../repository/TreasurygroupsRepository";
import { TreasurygroupsService } from "../service/TreasurygroupsService";
import { Treasurygroups } from "../entity/Treasurygroups";

class TreasurygroupsRest implements IControllerBase {
    public service = new TreasurygroupsService();
    public repository = getCustomRepository(TreasurygroupsRepository);
    public path = "/";
    public router = express.Router();

    constructor() {
        this.initRoutes();
    }

    public initRoutes() {
        this.router.get("/Treasurygroups/findall", this.findall);
        this.router.post("/Treasurygroups/find", this.find);
        this.router.post("/Treasurygroups/create", this.create);
        this.router.post("/Treasurygroups/save", this.save);
        this.router.delete("/Treasurygroups/delete", this.delete);
    }

    findall = async (req: Request, res: Response) => {
        try {
            var result = await this.service.findall();
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    find = async (req: Request, res: Response) => {
        try {
            var body: any = req.body;
            var result = await this.service.find(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    create = async (req: Request, res: Response) => {
        try {
            var body: Treasurygroups = req.body;
            var result = await this.service.create(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    save = async (req: Request, res: Response) => {
        try {
            var body: Treasurygroups = req.body;
            var result = await this.service.save(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    delete = async (req: Request, res: Response) => {
        try {
            var body: any = req.body;
            var result = await this.service.delete(body.id);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };
}

export default TreasurygroupsRest;
