import * as express from "express";
import { Request, Response } from "express";
import IControllerBase from "../framework/IControllerBase.interface";
import { getCustomRepository } from "typeorm";

import { ContactoSociedadRepository } from "../repository/ContactoSociedadRepository";
import { ContactoSociedadService } from "../service/ContactoSociedadService";
import { ContactoSociedad } from "../entity/ContactoSociedad";

class ContactoSociedadRest implements IControllerBase {
    public service = new ContactoSociedadService();
    public repository = getCustomRepository(ContactoSociedadRepository);
    public path = "/";
    public router = express.Router();

    constructor() {
        this.initRoutes();
    }

    public initRoutes() {
        this.router.get("/ContactoSociedad/findall", this.findall);
        this.router.post("/ContactoSociedad/find", this.find);
        this.router.post("/ContactoSociedad/create", this.create);
        this.router.post("/ContactoSociedad/save", this.save);
        this.router.delete("/ContactoSociedad/delete", this.delete);
    }

    findall = async (req: Request, res: Response) => {
        try {
            var result = await this.service.findall();
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    find = async (req: Request, res: Response) => {
        try {
            var body: any = req.body;
            var result = await this.service.find(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    create = async (req: Request, res: Response) => {
        try {
            var body: ContactoSociedad = req.body;
            var result = await this.service.create(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    save = async (req: Request, res: Response) => {
        try {
            var body: ContactoSociedad = req.body;
            var result = await this.service.save(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    delete = async (req: Request, res: Response) => {
        try {
            var body: any = req.body;
            var result = await this.service.delete(body.id);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };
}

export default ContactoSociedadRest;
