import * as express from "express";
import { Request, Response } from "express";
import IControllerBase from "../framework/IControllerBase.interface";
import { getCustomRepository } from "typeorm";

import { DocumentitemstatusesRepository } from "../repository/DocumentitemstatusesRepository";
import { DocumentitemstatusesService } from "../service/DocumentitemstatusesService";
import { Documentitemstatuses } from "../entity/Documentitemstatuses";

class DocumentitemstatusesRest implements IControllerBase {
    public service = new DocumentitemstatusesService();
    public repository = getCustomRepository(DocumentitemstatusesRepository);
    public path = "/";
    public router = express.Router();

    constructor() {
        this.initRoutes();
    }

    public initRoutes() {
        this.router.get("/Documentitemstatuses/findall", this.findall);
        this.router.post("/Documentitemstatuses/find", this.find);
        this.router.post("/Documentitemstatuses/create", this.create);
        this.router.post("/Documentitemstatuses/save", this.save);
        this.router.delete("/Documentitemstatuses/delete", this.delete);
    }

    findall = async (req: Request, res: Response) => {
        try {
            var result = await this.service.findall();
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    find = async (req: Request, res: Response) => {
        try {
            var body: any = req.body;
            var result = await this.service.find(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    create = async (req: Request, res: Response) => {
        try {
            var body: Documentitemstatuses = req.body;
            var result = await this.service.create(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    save = async (req: Request, res: Response) => {
        try {
            var body: Documentitemstatuses = req.body;
            var result = await this.service.save(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    delete = async (req: Request, res: Response) => {
        try {
            var body: any = req.body;
            var result = await this.service.delete(body.id);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };
}

export default DocumentitemstatusesRest;
