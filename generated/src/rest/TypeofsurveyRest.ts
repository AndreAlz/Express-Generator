import * as express from "express";
import { Request, Response } from "express";
import IControllerBase from "../framework/IControllerBase.interface";
import { getCustomRepository } from "typeorm";

import { TypeofsurveyRepository } from "../repository/TypeofsurveyRepository";
import { TypeofsurveyService } from "../service/TypeofsurveyService";
import { Typeofsurvey } from "../entity/Typeofsurvey";

class TypeofsurveyRest implements IControllerBase {
    public service = new TypeofsurveyService();
    public repository = getCustomRepository(TypeofsurveyRepository);
    public path = "/";
    public router = express.Router();

    constructor() {
        this.initRoutes();
    }

    public initRoutes() {
        this.router.get("/Typeofsurvey/findall", this.findall);
        this.router.post("/Typeofsurvey/find", this.find);
        this.router.post("/Typeofsurvey/create", this.create);
        this.router.post("/Typeofsurvey/save", this.save);
        this.router.delete("/Typeofsurvey/delete", this.delete);
    }

    findall = async (req: Request, res: Response) => {
        try {
            var result = await this.service.findall();
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    find = async (req: Request, res: Response) => {
        try {
            var body: any = req.body;
            var result = await this.service.find(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    create = async (req: Request, res: Response) => {
        try {
            var body: Typeofsurvey = req.body;
            var result = await this.service.create(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    save = async (req: Request, res: Response) => {
        try {
            var body: Typeofsurvey = req.body;
            var result = await this.service.save(body);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };

    delete = async (req: Request, res: Response) => {
        try {
            var body: any = req.body;
            var result = await this.service.delete(body.id);
            return res.status(200).json({ data: result });
        } catch (e) {
            return res.status(500).json({ error: e.message });
        }
    };
}

export default TypeofsurveyRest;
